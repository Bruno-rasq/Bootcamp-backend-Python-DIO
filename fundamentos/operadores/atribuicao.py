# operadores de atribuição

'''
operadores de atribuição sao usados para atribuir valores a variáveis

por exemplo 

a = 10

variavel a recebe o valor 10

porem ha outros operadores de atribuição

+= atribui a variavel o valor da soma dela mesma com o valor da variavel

-= atribui a variavel o valor da subtração dela mesma com o valor da variavel

/= atribui a variavel o valor da divisão dela mesma com o valor da variavel

*= atribui a variavel o valor da multiplicação dela mesma com o valor da variavel

//= atribui a variavel o valor da divisão inteira dela mesma com o valor da variavel

%= atribui a variavel o valor do resto da divisão dela mesma com
'''

a = 10 # a == 10
a += 5 # a == 15
a -= 3 # a == 12
a /= 2 # a == 6
a *= 2 # a == 12
a //= 3 # a == 4

print(a)
 