#operadores de comparação 

'''
 > maior que
 < menor que
 >= maior ou igual
 <= menor ou igual
 == igual
 != diferente

 podem ser usados tanto para variáveis do tipo number, string ou boolean
 
'''

print("a" == "a")
print("a" == "b")

print(10 > 5)
print(10 <= 5)

print(True == 1)
print(False == 0)
print(True == 1.0)

print("B" != 2)

